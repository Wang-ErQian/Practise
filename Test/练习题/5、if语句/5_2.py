# 编写一系列条件测试；将每个测试以及你对其结果的预测和实际结果都打印出来
car = 'Bwm'

# 检查两个字符串相等和不等
print(car == 'audi')
print(car != 'bwm')

# 使用函数 lower() 的测试
print(car.lower() == 'bwm')

# 检查两个数字相等、不等、大于、小于、大于等于和小于等于
print(1 == 1)
print(1 != 2)
print(1 > 2)
print(1 < 2)
print(1 >= 2)
print(1 <= 2)

print(1 < 2 and 1 < 3)
print(1 > 2 or 1 < 3)
list_ = ['audi']
print('audi' in list_)
print('bmw' not in list_)
