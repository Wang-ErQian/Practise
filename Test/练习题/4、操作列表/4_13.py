# 有一家自助式餐馆，只提供五种简单的食品。请想出五种简单的食品，并将其存储在一个元组中。
foods = ('鸡', '鸭', '鱼', '鹅', '猪')
# 使用一个 for 循环将该餐馆提供的五种食品都打印出来
for food in foods:
    print(food)
